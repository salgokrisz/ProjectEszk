package cluedo.logic.fields;

/**
 *
 * @author Bence
 */
public abstract class Field {
    protected int x;
    protected int y;
    
    public Field(){
        
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
